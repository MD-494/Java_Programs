public class Subtraction {
    public static void main(String[] args) {

        /** Declare two integer variables */
        int a = 50;
        int b = 25;

        /** Calculates subtraction of a & b and stores it in the c integer variable */
        int c = a - b;

        /** Print the result */
        System.out.println("Subtraction is " + c);
    }
}